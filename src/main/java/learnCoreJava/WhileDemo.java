package learnCoreJava;

public class WhileDemo {

    public static void main(String[] args) {

        //While loop
        int i = 10;
        while(i > 0){
            System.out.println(i);
            i--;
        }

        // do while

        int j = 20;
        do{
            j++;
            System.out.println(j);
        }while(j < 30);


    }


}
